/**
 * ターン値を配列インデックスに変換
 * @param {number} turn - ターン (1=アタック, -1=ディフェンス)
 * @returns {number} 配列インデックス (0=ディフェンス, 1=アタック)
 */
export function arrayTurn(turn) {
	return (turn + 1) / 2;
}

